/* risergamepad-gui.c -- fullscreen GUI variant of the learn command.
 *
 * Opens a custom 320x256 screen with a small fixed palette and a
 * borderless backdrop window covering it.  Draws a stylised pixel-art
 * CD32 gamepad and walks the user through mapping each colour /
 * function button on the picture to a button on their USB pad.
 *
 * Flow is fully gamepad-driven:
 *   button held  = capture for current slot
 *   D-pad DOWN   = skip this slot (leave unmapped)
 *   D-pad LEFT + any button = exit
 *
 * After all slots are captured the mappings are committed to the
 * firmware (auto-saved per slot) and the screen stays open in verify
 * mode -- pressing buttons on the gamepad lights up the mapped
 * picture buttons so the user can confirm everything.
 */

#include "amiga_min.h"
#include "cd32_bitmap.h"
#include <stdio.h>
#include <string.h>

/* Static BitMap pointing at the embedded planar data from
 * cd32_bitmap.c (auto-generated by AmigaTools/cd32_make_bitmap.py). */
static struct BitMap cd32_bm = {
    CD32_BMP_BWIDTH,
    CD32_BMP_H,
    0,
    CD32_BMP_DEPTH,
    0,
    {
        (PLANEPTR)0L,   /* filled in at runtime to avoid static-init
                         * const-vs-non-const cast warnings */
        0L, 0L, 0L, 0L, 0L, 0L, 0L
    }
};

extern const unsigned char cd32_plane0[];
extern const unsigned char cd32_plane1[];
extern const unsigned char cd32_plane2[];
extern const unsigned char cd32_plane3[];

/* Riser memory map -- same as risergamepad.c. */
#define RISER_BASE_ADDR  0x00BA0000UL
#define SLOTS            10
#define UNMAPPED         0xFFu
#define PAD1_REG_BASE    0x20
#define PAD2_REG_BASE    0x2A
#define PAD1_LIVE_DATA   0x10
#define PAD1_LIVE_EXTRA  0x11
#define PAD2_LIVE_DATA   0x12
#define PAD2_LIVE_EXTRA  0x13
#define SENTINEL_REG     0x16   /* reads 0xA5 when the riser bus path is alive */
static volatile unsigned char * const riser_gui =
    (volatile unsigned char *)RISER_BASE_ADDR;

#define CAP_ABORT  -1
#define CAP_SKIP   -2

#define MASK_LEFT     0x0002u
#define MASK_DOWN     0x0004u
#define MASK_DPAD     0x000Fu
#define MASK_BUTTONS  0xFFF0u

struct IntuitionBase *IntuitionBase = 0L;
struct GfxBase       *GfxBase       = 0L;

int cmd_gui(int pad, int demo);

/* ---- screen + palette ------------------------------------------------ */

/* Hi-Res 640x256, 4 bitplanes = 16 colours.  256 rows fully covers a
 * PAL display (no white strip below) while NTSC monitors simply clip
 * the bottom -- all visible content lives in the top 200 rows.  The
 * 640x140 photo is blitted at the top; the dark navy text strip runs
 * from y=140 all the way to the bottom so PAL doesn't show any gap. */
#define SCR_W   640
#define SCR_H   256
/* Push the photo down so the Workbench/screen title bar doesn't clip
 * the top of the controller. */
#define BMP_Y   20
#define SCR_D   CD32_BMP_DEPTH

/* Pens.  Indices 0..8 are filled by the generator (white, black,
 * greys, red, green, yellow, blue).  Indices 9..15 are unused in
 * the source image but we steal a few of them for UI overlays. */
#define PEN_WHITE   0
#define PEN_BLACK   1
#define PEN_GREY_M  2
#define PEN_GREY_D  3
#define PEN_GREY_L  4
#define PEN_RED     5
#define PEN_GREEN   6
#define PEN_YELLOW  7
#define PEN_BLUE    8
#define PEN_HIGHL   9   /* overlay highlight ring */
#define PEN_TXT_BG 10   /* dark blue strip behind prompt text */
#define PEN_TXT_FG 11   /* bright text on the strip */

/* Overlay-pen palette overrides applied after LoadRGB4. */
static void install_overlay_pens(struct ViewPort *vp)
{
    /* Bright purple highlight ring -- magenta-leaning (max red, less
     * blue) so it pops against the dark blue-grey controller body. */
    SetRGB4(vp, (LONG)PEN_HIGHL, (ULONG)15, (ULONG)0, (ULONG)12);
    /* Dark navy strip behind prompt text */
    SetRGB4(vp, (LONG)PEN_TXT_BG, (ULONG)1, (ULONG)1, (ULONG)3);
    SetRGB4(vp, (LONG)PEN_TXT_FG, (ULONG)15, (ULONG)15, (ULONG)15);
}

